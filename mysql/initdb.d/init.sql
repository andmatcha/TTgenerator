DROP DATABASE IF EXISTS tt;

CREATE DATABASE tt;

USE tt;

DROP TABLE IF EXISTS members;

DROP TABLE IF EXISTS member_details;

DROP TABLE IF EXISTS parts;

DROP TABLE IF EXISTS bands;

DROP TABLE IF EXISTS band_members;

DROP TABLE IF EXISTS timeframes;

DROP TABLE IF EXISTS not_attend;

CREATE TABLE members (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `name` VARCHAR(225) NOT NULL
);

-- とりあえず今回は使わない
CREATE TABLE member_details (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `member_id` INT NOT NULL,
  `part_id` INT NOT NULL
);

CREATE TABLE parts (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `name` VARCHAR(225) NOT NULL
);

CREATE TABLE bands (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `name` VARCHAR(225) NOT NULL
);

CREATE TABLE band_members (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `band_id` INT NOT NULL,
  `member_id` INT NOT NULL
);

CREATE TABLE timeframes (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `start_time` DATETIME,
  `length` INT NOT NULL DEFAULT 30
);

CREATE TABLE not_attend (
  `id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  `member_id` INT NOT NULL,
  `timeframe_id` INT NOT NULL
);

